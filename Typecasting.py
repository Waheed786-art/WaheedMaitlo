# Typecasting is used to convert one data type into another..
# Mathematical operation will only be possible if numbers are in string
# There are two types of typecasting:  1: Explicit which is written by programmer   2: Implicit which is automatically added by python.
#  

a="2" # Here 2 is a string
b="5" # Here 5 is a string

print(int(a)+int(b)) # Here the value of a and b is converted into integer.. it is converted by me means it is explicit typecasting.


